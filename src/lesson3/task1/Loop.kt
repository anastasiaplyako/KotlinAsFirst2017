 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 * решение =
 * ревернуть число
 * н - 6 while x/10
 *% 10
 */

fun squareSequenceDigit(n: Int): Int {
    var str = 0
    var number = 1
    var length = 0
    while (length < n) {
        str = number * number
        length += digitNumber(str)
        number++
    }
    while (length != n) {
        str /= 10
        length--
    }
    return str % 10
}

